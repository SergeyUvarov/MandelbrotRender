COLORREF getPalette(double n, int32_t p = 0)
{
    int paletSize = 256;
    auto palet = PLASMA_PALETTE;

    switch (p)
    {
    case 0:
        paletSize = 500;
        palet = PALETTE;
        break;

    case 1:
        paletSize = 255;
        palet = MAGMA_PALETTE;
        break;

    case 2:
        paletSize = 256;
        palet = PLASMA_PALETTE;
        break;
    }

    int n_1 = int(floor(n * paletSize)) % paletSize;
    int n_2 = int(ceil(n * paletSize)) % paletSize;

    int8_t r = palet[n_1][0] + (palet[n_2][0] - palet[n_1][0]) * n;
    int8_t g = palet[n_1][1] + (palet[n_2][1] - palet[n_1][1]) * n;
    int8_t b = palet[n_1][2] + (palet[n_2][2] - palet[n_1][2]) * n;

    return sRGB(r, g, b);
    return sRGB(0, 0, 0);
}